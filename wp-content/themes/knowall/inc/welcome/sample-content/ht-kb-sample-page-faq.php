<p>Create a Frequently Asked Questions page with KnowAll.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>From your WordPress dashboard select <em>Pages &gt; Add New</em>. Set the title to <strong>FAQ</strong>.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Then add your FAQs, using the Toggle block found in the included Heroic Blocks plugin. The result will look like the content below:-</p>
